# Range function with For Loop

# Doesn't do anything
print(range(1, 10))

# Prints 1 to 9
for number in range(1, 10): 
    print(number)

# Prints 1 to 10
for number in range(1, 11):  
    print(number)

# Prints 1 to 10, in increments of 3 (i.e. 1,4,7..)
for number in range(1,11,3):
    print(number)
